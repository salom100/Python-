'''
	Diz respeito a operadores do tipo AND OR e NOT
	AND : se os dois forem verdadeiros entao retorna TRUE
	OR : se um ou o outro for verdadeiro ele retorna TRUE
	NOT : se o valor nao for igual ele retorna Falso
'''
